package com.niit.stringsandarrays;
public class HelloWorldUnicode { 
public static void main(String [] args) { 
	String strTamil = "காலை வணக்கம்"; 
	String strHindi = "शुभ प्रभात";
	String strMarathi = "सुप्रभात" ; 
	String strPunjabi = "ਸ਼ੁਭ ਸਵੇਰ"; 
	String strChinese  = "早上好";
	System.out.println(strTamil); 
	System.out.println(strHindi);
	System.out.println(strMarathi);
	System.out.println(strPunjabi);
	System.out.println(strChinese);
} 
} 
